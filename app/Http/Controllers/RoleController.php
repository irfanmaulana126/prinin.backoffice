     */
    public function store(Request $request)
    {
        $input = $request->all();
        $validator = Validator::make($input,[
            'user_id'=>'required|string|max:255',
            'user_level_id'=>'required|string|max:255',
        ]);

        if ($validator->fails()) {
            return redirect('master/userrole/create')->withInput()->withErrors($validator);
        }
        UserRole::create($input);
        return redirect('master/userrole');
    }

